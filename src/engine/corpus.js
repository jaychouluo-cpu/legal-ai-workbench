// 关键词全文检索：按命中字段加权打分，返回带相关度的结果
export function searchCorpus(query, kind /* 'all' | '法规' | '案例' */) {
  const q = (query || '').trim()
  const tokens = q.split(/[\s,，、]+/).filter(Boolean)
  let list = CORPUS
  if (kind === '法规') list = list.filter((c) => c.kind === '法规')
  else if (kind === '案例') list = list.filter((c) => c.kind === '案例')

  if (!tokens.length) {
    return list.map((c) => ({ ...c, rel: 0 }))
  }

  const scored = list.map((c) => {
    const hay = (c.title + ' ' + c.gist + ' ' + c.body)
    let score = 0
    for (const tk of tokens) {
      if (c.title.includes(tk)) score += 5
      if (c.gist.includes(tk)) score += 3
      if (c.body.includes(tk)) score += 2
      // 子串部分命中
      else if (hay.includes(tk)) score += 1
    }
    const rel = Math.min(99, Math.round((score / (tokens.length * 5)) * 60) + (score > 0 ? 38 : 0))
    return { ...c, rel, _score: score }
  })
  return scored.filter((c) => c._score > 0).sort((a, b) => b._score - a._score)
}
